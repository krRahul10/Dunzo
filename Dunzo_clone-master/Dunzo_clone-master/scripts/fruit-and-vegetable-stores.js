  productDataDisplay=()=>{

    let FV_Data = JSON.parse(localStorage.getItem("FV_Data"))//fetch data;
    
    //maping data using for each loop
    FV_Data.forEach(el => {

    // main div
    let div = document.createElement('div');
     div.addEventListener("click",function(){
        goToAnatherPage(el)
     });

    let div2 = document.createElement('span');
    div2.setAttribute('class','nddiv');
    // create image tag and give src
    let image = document.createElement('img');
    image.setAttribute("src",el.Main_img_url);
    image.setAttribute("class",'Main_img_T');//classcreating for clock img

    //Name creation
    let name = document.createElement('h2');
    name.textContent = el.name;

    //Location maping
    let location = document.createElement('p');
    location.textContent = el.location;
    location.setAttribute('class','location_T')
    

    //Km mapping
    let km = document.createElement('span');
    km.textContent = el.km;
    km.setAttribute("class",'km_style_t')

    // clock img
    let c_img = document.createElement('img');
    c_img.setAttribute("src",el.c_img);
    c_img.setAttribute("class",'cimg_T');//classcreating for clock img

    //min mapping
    let min = document.createElement('span');
    min.textContent = el.min;
    min.setAttribute('class','min_t')

    div2.append(name,location,km,c_img,min)
    //append all items with div
    div.append(image,div2)
    // append div with container
    document.querySelector('#container_fv_T').append(div)

  });
 }
